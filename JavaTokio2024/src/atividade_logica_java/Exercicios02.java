package atividade_logica_java;

public class Exercicios02 {

	public static void main(String[] args) {
		int num1 = 5;
		float num2 = 5.4f;
		Double num3 = 5.3;
		String num1str = Integer.toString(num1);
		String num2str = Float.toString(num2);
		String num3str = Double.toString(num3);
		
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num1str +" | "+ num2str + " | " + num3str);
	}

}
